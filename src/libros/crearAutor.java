    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = txtNombre.getText().trim(); // Trim para eliminar espacios en blanco al inicio y al final

        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el nombre del autor.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Autor autor = new Autor(nombre);
        guardarAutorEnArchivo(autor);
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void guardarAutorEnArchivo(Autor autor) {
        try {
            File file = new File("src/data/autores.txt");
            if (!file.exists()) {
                file.getParentFile().mkdirs();
                file.createNewFile();
            }

            BufferedWriter bw = new BufferedWriter(new FileWriter(file, true));
            bw.write(autor.getNombre() + "\n");
            bw.close();
            JOptionPane.showMessageDialog(this, "Autor guardado correctamente.", "Guardado", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            new menu().setVisible(true);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar el autor.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

